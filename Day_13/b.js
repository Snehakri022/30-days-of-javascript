var numbers = [1, 2, 3, 4, 5];

// splice
document.write(numbers.splice(1,3) +"<br>");

// push
numbers.push(6);
document.write(numbers +"<br>");

// pop
numbers.pop();
document.write(numbers +"<br>");

// unshift
numbers.unshift(7);
document.write(numbers +"<br>");

// shift
numbers.shift();
document.write(numbers +"<br>");